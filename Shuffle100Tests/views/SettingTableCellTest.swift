import XCTest

class SettingTableCellTest: XCTestCase {
    let screen = ReciteSettingsScreen()

    override func setUpWithError() throws {
        screen.loadViewIfNeeded()
    }

    override func tearDownWithError() throws {
        // Put teardown code here. This method is called after the invocation of each test method in the class.
    }

    func test_firstCellConfigurationIsSet() throws {
        let firstCell = screen.tableView(screen.tableView, cellForRowAt: IndexPath(row: 0, section: 0))
        let cellCoonfig = firstCell.contentConfiguration as? UIListContentConfiguration ?? nil
        XCTAssertNotNil(cellCoonfig)
        XCTAssertEqual(cellCoonfig?.text, "歌と歌の間隔")
        XCTAssertEqual(cellCoonfig?.secondaryText, "1.10"+"秒")
    }


}
